package primeiros10;

public class DataTypePratice {
    public static void main(String[] args) {
        // Declare variables of types int, double, boolean, and String, and print their values.
        var age = 30; // Integer type
        var salary = 50000.50D; // Double type var salary = 50000.50F; // flout type
        var isEmployed = true; // Boolean type
        var name = "John Doe"; // String type

                // Print the values of the variables
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Is Employed: " + isEmployed);
        System.out.println("Name: " + name);
    }
}
